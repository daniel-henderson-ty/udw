func TestMustDecodeStringToByteArrayIgnoreNonHex(ot *testing.T) {
	b := MustDecodeStringToByteArrayIgnoreNonHex(`|   02 78 19 00  11 97 b2 c3  3e 92 63 17  69 a0 98 8e
|   05 10 02 01  00 00 00 00  00 00 00 6c  bd 3c dc 59
|   2c d8 2d ca  69 87 b3 01  09 c8 2e 23  d3 c6 05 93
|   ea 81 b4 e3  5c 27 d2 76  b8 15 67 f3  20 1c d6 15
|   55 03 1d cd  8a 58 10 b1  19 ef 35 ab  80 d6 02 cf
|   6d 37 e6 6e  04 4f 51 45  3c 8b ef 33  44 94 9e fb
|   f1 99 85 10  65 66 fa b7  e9 39 50 a2`)
	udwTest.Equal(b, []byte{0x02, 0x78, 0x19, 0x00, 0x11, 0x97, 0xb2, 0xc3, 0x3e, 0x92, 0x63, 0x17, 0x69, 0xa0, 0x98, 0x8e,
		0x05, 0x10, 0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x6c, 0xbd, 0x3c, 0xdc, 0x59,
		0x2c, 0xd8, 0x2d, 0xca, 0x69, 0x87, 0xb3, 0x01, 0x09, 0xc8, 0x2e, 0x23, 0xd3, 0xc6, 0x05, 0x93,
		0xea, 0x81, 0xb4, 0xe3, 0x5c, 0x27, 0xd2, 0x76, 0xb8, 0x15, 0x67, 0xf3, 0x20, 0x1c, 0xd6, 0x15,
		0x55, 0x03, 0x1d, 0xcd, 0x8a, 0x58, 0x10, 0xb1, 0x19, 0xef, 0x35, 0xab, 0x80, 0xd6, 0x02, 0xcf,
		0x6d, 0x37, 0xe6, 0x6e, 0x04, 0x4f, 0x51, 0x45, 0x3c, 0x8b, 0xef, 0x33, 0x44, 0x94, 0x9e, 0xfb,
		0xf1, 0x99, 0x85, 0x10, 0x65, 0x66, 0xfa, 0xb7, 0xe9, 0x39, 0x50, 0xa2})
}
